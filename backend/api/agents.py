def optimizer_node(state: AgentState):
    llm = init_llm()
    messages = state['messages']
    prompt = SystemMessage(content="You are the Optimizer Agent. Analyze the error logs. Output the code fix necessary.")
    response = llm.invoke([prompt] + messages)
    
    # Auto-Remediation PR
    repo = os.environ.get('TARGET_GITHUB_REPO', 'yourusername/target-repo')
    branch = create_branch(repo, "fix/auto-remediation")
    fixed_code = extract_code_block(response.content)
    commit_file(repo, branch, "fixed_file.py", fixed_code, "Optimizer Agent: Auto-remediation formatting/logic fix")
    
    pr_url = open_pull_request(repo, branch, "Auto-Remediation Formatter Fix", "Generated by Optimizer Agent via DevOps Copilot based on log analysis.")
    
    pr_msg = AIMessage(content=f"I analyzed the logs and created an auto-remediation PR for your approval: {pr_url}")
    return {"messages": [pr_msg], "github_pr_url": pr_url, "requires_approval": True}

# Routing function
def route_after_warden(state: AgentState):
    current = state.get("current_agent")
    if current == 'Data Engineer':
        return 'data_engineer'
    elif current == 'SRE/DevOps':
        return 'sre'
    elif current == 'Optimizer':
        return 'optimizer'
    return 'architect'

def build_graph():
    workflow = StateGraph(AgentState)
    
    workflow.add_node("warden", warden_node)
    workflow.add_node("architect", architect_node)
    workflow.add_node("data_engineer", data_engineer_node)
    workflow.add_node("sre", sre_node)
    workflow.add_node("optimizer", optimizer_node)
    
    workflow.set_entry_point("warden")
    
    workflow.add_conditional_edges("warden", route_after_warden, {
        "data_engineer": "data_engineer",
        "sre": "sre",
        "optimizer": "optimizer",
        "architect": "architect"
    })
    
    workflow.add_edge("architect", END)
    workflow.add_edge("data_engineer", END)
    workflow.add_edge("sre", END)
    workflow.add_edge("optimizer", END)
    
    return workflow.compile()

# Dependency injection for the SQS consumer
def invoke_swarm(initial_message: str):
    app = build_graph()
    state = {
        "messages": [HumanMessage(content=initial_message)],
        "project_id": 1, 
        "sender": "webhook",
        "channel": "slack/discord"
    }
    
    result = app.invoke(state)
    return result['messages'][-1].content
